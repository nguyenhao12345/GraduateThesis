import Foundation
import UIKit

extension CGFloat {
    
    var double: Double {
        return Double(self)
    }
    
    var float: Float {
        return Float(self)
    }
    
    var int: Int {
        return Int(self)
    }
    
    var string: String {
        return "\(self)"
    }
}
